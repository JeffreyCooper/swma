			<p>The new SWMA website will focus on the following areas:</p>
			<ol>
				<li>Catalog: Provide a complete catalog of the SWMA artwork. It must be searchable. It must contain high-quality images of the artwork. It must contain descriptions of each piece of art. It must represent the various Series and the art representing each.</li>
				<li>Customers: The website should present to customers the work of SWMA. It must include an option for purchases via mail online.</li>
				<li>Wholesalers: The website should present wholesalers with bulk ordering information.</li>
				<li>Contact information: There will be a contact form with contact details to reach SWMA.</li>
			</ol>
			<h5>Additional Website Features</h5>
			<p>Accounts: Login - Each active customer can login to the website.</p>
			<ol>
				<li>Customer information retained through a detailed login procedure.</li>
				<li>Each customer must be distinguished as either an INDIVIDUAL or a COMPANY. The former will be considered retail while the later will be considered Wholesale.</li>
				<li>A COMPANY must inlude their state issued CRS number to be kept on record, which will entitle them to being charged without sales tax in NM.</li>
				<li>A COMPANY that is NOT in NM will be afforded Wholesale status by including their TIN number. By doing so, they will not be charged Sales Tax.</li>
				<li>An INDIVIDUAL who is NOT a resident of NM will not be charged tax. However, they should be informed that they could be subject to tax in the future if the state decides to change the law, as they tried in 2017.</li>
				<li>Each entity creating an account must agree to be contacted by SWMA for any company or account related news.</li>
				<li>An opt-in feature will allow the creation of a mailing list of INDIVIDUALS.</li>
				<li>All COMPANY accounts will be added to a mailing list for Wholesalers. This is automatic, without an opt-in feature. There must be a disclosure of this list during sign-up.</li>
			</ol>
